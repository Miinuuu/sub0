"""Build LaTeX appendix table backing Fig 2.

Consolidates cap_sweep_v7/v8/v9 raw data into one table:
  N | B | FA2 tok/s | CDA tok/s | Gain | prompt | decode

Usage:
    python papers/gen_tab_fig2_raw.py \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N4K.json \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N16K.json \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N64K.json \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N128K.json \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N16K_cda_high.json \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N64K_cda_v2.json \
        runs/paper/cap_sweep_v7_Bsweep_decodebound/N128K_cda_v2.json \
        runs/paper/cap_sweep_v8_cda_high_B/*.json \
        runs/paper/cap_sweep_v9_fa2_oom_cliff/*.json
"""
from __future__ import annotations
import argparse
import json
from pathlib import Path


def _N_label(n: int) -> str:
    if n >= 1024 * 1024:
        return f"{n // (1024*1024)}M"
    if n >= 1000:
        return f"{n // 1024}K"
    return str(n)


def _consolidate(paths: list[str]) -> dict:
    by_ctx: dict[int, dict] = {}
    for path in paths:
        try:
            d = json.loads(Path(path).read_text())
        except Exception as e:
            print(f"WARN skipping {path}: {e}")
            continue
        cfg = d["config"]
        P, D = cfg["prompt_len"], cfg["decode_len"]
        ctx = P + D
        for power in (4096, 8192, 16384, 65536, 131072):
            if abs(ctx - power) / power < 0.05:
                ctx_key = power
                break
        else:
            ctx_key = ctx
        if ctx_key not in by_ctx:
            by_ctx[ctx_key] = {"FA2": {}, "CDA": {}}
        for backend in ("FA2", "CDA"):
            for r in d.get(backend, []):
                if r.get("status") != "ok":
                    continue
                B = r["batch"]
                wall = r.get("wall_s", 0)
                if not wall or not B:
                    continue
                out_tps = B * D / wall
                cur = by_ctx[ctx_key][backend].get(B)
                if cur is None or out_tps > cur["out_tps"]:
                    by_ctx[ctx_key][backend][B] = {
                        "B": B, "out_tps": out_tps, "wall_s": wall,
                        "prompt_len": P, "decode_len": D,
                    }
    return by_ctx


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("artifacts", nargs="+")
    ap.add_argument("--output-tex", default="papers/tab_fig2_raw.tex")
    args = ap.parse_args()

    by_ctx = _consolidate(args.artifacts)

    # Build rows: per N, sorted by B; show only batches present in BOTH FA2 and CDA,
    # plus CDA-only "capacity" rows at higher B (FA2 OOM)
    out_lines = [
        "% Auto-generated by papers/gen_tab_fig2_raw.py",
        "\\begin{table}[t]",
        "\\centering\\footnotesize",
        "\\caption{Raw data backing Fig.~\\ref{fig:throughput}. Decode-bound output "
        "throughput (tokens/s, excluding prefill) for vLLM serving on Llama-3.1-8B / RTX A6000. "
        "Prompt and decode lengths are listed per context; all cells are decode-dominant in wall time. "
        "Both backends were swept up to the highest $B$ that fits FA2's FP16 KV pool at "
        "\\texttt{gpu\\_memory\\_utilization}=0.85 on a single $48$\\,GB device.}",
        "\\label{tab:fig2-raw}",
        "\\setlength{\\tabcolsep}{4pt}",
        "\\begin{tabular}{@{}rrrr|rrr@{}}",
        "\\toprule",
        "$\\nctx$ & prompt & decode & $B$ & FA2 (tok/s) & \\cda{} (tok/s) & Gain \\\\",
        "\\midrule",
    ]

    sorted_ctx = sorted(by_ctx.keys())
    for ctx in sorted_ctx:
        fa2_data = by_ctx[ctx]["FA2"]
        cda_data = by_ctx[ctx]["CDA"]
        all_b = sorted(set(fa2_data.keys()) | set(cda_data.keys()))
        first_row = True
        for B in all_b:
            fa2 = fa2_data.get(B)
            cda = cda_data.get(B)
            # Use prompt/decode from whichever is present
            ref = cda or fa2
            P, D = ref["prompt_len"], ref["decode_len"]
            ctx_label = _N_label(ctx) if first_row else ""
            P_str = f"{P}" if first_row else ""
            D_str = f"{D}" if first_row else ""
            first_row = False
            fa2_str = f"{fa2['out_tps']:.1f}" if fa2 else "--"
            cda_str = f"{cda['out_tps']:.1f}" if cda else "--"
            if fa2 and cda:
                gain = (cda['out_tps'] / fa2['out_tps'] - 1.0) * 100.0
                gain_str = f"$+{gain:.1f}\\%$" if gain >= 0 else f"${gain:.1f}\\%$"
            elif cda and not fa2:
                gain_str = "FA2 OOM"
            else:
                gain_str = "--"
            out_lines.append(
                f"{ctx_label} & {P_str} & {D_str} & {B} & {fa2_str} & {cda_str} & {gain_str} \\\\"
            )
        out_lines.append("\\midrule")

    # Drop trailing midrule, add bottomrule
    if out_lines[-1] == "\\midrule":
        out_lines.pop()
    out_lines.extend([
        "\\bottomrule",
        "\\end{tabular}",
        "\\end{table}",
    ])

    out = Path(args.output_tex)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("\n".join(out_lines) + "\n")
    print(f"Saved: {out}")
    print(f"Cells: {sum(len(by_ctx[c]['FA2']) + len(by_ctx[c]['CDA']) for c in by_ctx)}")


if __name__ == "__main__":
    main()
